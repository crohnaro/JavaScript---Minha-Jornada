/*

adição: +
subtração: -
multiplicação: *
divisão: /
incremento: ++  
decremento: --

atribuição: +=   -=

*/

// adição 
let a = 10
let b = 5
let c = a + b
console.log(c)


// subtração
let d = 1
let e = 10
let f = d - e
console.log(f)


// multiplicação
let g = 12
let h = 6
let i = g * h
console.log(i)


// divisão
let j = 19
let k = 3
let l = j / k
console.log(l)


// incremento
let m = 10
m++
console.log(m)


// decremento
let n = 10
n--
console.log(n)


// atribuição  +
let o = 12
o += 25  // o = o + 25
console.log(o)

// atribuição  -
let p = 12
p -= 25  // p = p - 25
console.log(p)